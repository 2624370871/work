package com.java;

public class Demo2 {
private int id;//긍뵀
private String name;//蝎츰
private double price;//송목
private int storage;//욋닸
//唐꽝뭐芚
public Demo2(int id,String name,double price,int storage) {
	this.id=id;
	this.name=name;
	this.price=price;
	this.storage=storage;
}
//삿혤蝎뵀
public int getId() {
	return id;
}
//삿혤蝎츰
public String getName() {
	return name;
}
//삿혤송목
public double getPrice() {
	return price;
}
//삿혤욋닸
public int getStorage() {
	return storage;
}
}
